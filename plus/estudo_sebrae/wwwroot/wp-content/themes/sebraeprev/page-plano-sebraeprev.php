<p><b style="color: #4a7ebb;">Regime Regressivo</b></p>
<p>► Utilizado para cálculo de IR exclusivo na fonte – sem ajuste na declaração anual;</p>
<p>► Nessa opção é levado em conta o prazo de acumulação dos recursos que não é igual ao prazo de filiação ao plano;</p>
<p>► Cada contribuição ao plano faz “aniversário”. Por exemplo, para chegar à alíquota de 10%, é preciso que todas as contribuições completem 10 anos. A contagem do prazo de acumulação continua mesmo depois do início do recebimento do benefício de aposentadoria;</p>
<p>► Para efeito de resgate será aplicada uma fórmula para apurar o prazo de acumulação, que é conhecida como PEPS (primeiro aporte a entrar é o primeiro a sair).</p>
<p>Definir qual regime de tributação é mais vantajoso, deve ser uma avaliação pessoal e exclusiva do Participante. Os pontos mais relevantes a serem considerados são: o prazo de acumulação dos recursos, o tempo de permanência no Plano, o valor do benefício estimado, a forma e o prazo de recebimento do benefício ou do resgate, a renda total recebida na aposentadoria e os possíveis abatimentos da renda tributável.</p>
