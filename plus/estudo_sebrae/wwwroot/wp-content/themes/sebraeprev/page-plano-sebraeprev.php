<p>O SEBRAEPREV é um Plano de benefícios voltado especificamente para você, empregado, gerente, diretor, conselheiro ocupante de cargo eletivo e dirigente das Unidades que compõem o Sistema SEBRAE, e tem por objetivo proporcionar uma renda mensal complementar ao benefício de aposentadoria que é pago pelo INSS, proporcionando mais segurança e qualidade de vida a você e seus familiares.</p>
<p>No resumo apresentado abaixo, você fica sabendo sobre as contribuições, os benefícios e as opções que tem caso saia da sua Patrocinadora. Importante: mais detalhes estão disponíveis no Regulamento do Plano SEBRAEPREV. As informações do quadro estão resumidas e podem conter outras condições.</p>
<p style="text-align: center;"><a href="http://sebraeprevidencia.com.br/wp-content/uploads/2013/07/plano-sebraeprev1.jpg" ><img class="size-full wp-image-5447 aligncenter" alt="plano sebraeprev1 Plano SEBRAEPREV" src="http://sebraeprevidencia.com.br/wp-content/uploads/2013/07/plano-sebraeprev1.jpg" width="561" height="1000" title="Plano SEBRAEPREV" /></a></p>
<div class="accordian"><h5 class="toggle "><a href="#"><span class="arrow"></span>Sobre a administradora do SEBRAEPREV</a></h5><div class="toggle-content " style="">O SEBRAEPREV é administrado pelo SEBRAE PREVIDÊNCIA, uma Entidade Fechada de Previdência Complementar, constituída sob a forma de sociedade civil, sem fins lucrativos, de personalidade jurídica de direito privado, criada pelo Serviço Brasileiro de Apoio às Micro e Pequenas Empresas – SEBRAE.</div><h5 class="toggle "><a href="#"><span class="arrow"></span>Características básicas do Plano</a></h5><div class="toggle-content " style="">O SEBRAEPREV é um plano de benefícios do tipo Contribuição Definida (CD), o que significa que o Participante define o valor das contribuições mensais para o plano, que corresponde a um percentual que varia de 1% a 7% sobre o seu salário. Para cada percentual que você escolher, o Sebrae também depositará o mesmo valor. Este valor será depositado em conta individual, aberta em nome do participante junto ao SEBRAEPREV.</p>
<p>O saldo da conta acumulado em nome do Participante é composto pelas contribuições pessoais, do Patrocinador e a rentabilidade auferida na aplicação desses recursos.</p>
<p>Os Patrocinadores do Plano são o SEBRAE Nacional e as Unidades que compõem o Sistema SEBRAE, a ABASE e o próprio SEBRAE Previdência.</p>
<p>Esse Plano foi desenhado exclusivamente para você que faz parte do Sistema SEBRAE. Por isso é denominado Plano Fechado.</div><h5 class="toggle "><a href="#"><span class="arrow"></span>O Plano SEBRAEPREV em números</a></h5><div class="toggle-content " style="">
