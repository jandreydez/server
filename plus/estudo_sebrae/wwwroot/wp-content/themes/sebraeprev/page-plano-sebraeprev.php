<p>A equipe do SEBRAE PREVIDÊNCIA está à disposição para tirar as suas dúvidas. Além disso, todas as sedes do SEBRAE têm Gestores do Plano SEBRAEPREV capacitados para prestar atendimento e fazer a sua adesão. Caso queira saber quem é o Gestor no seu SEBRAE, entre em contato conosco.</p>
<p style="text-align: center;"><strong>O SEBRAE PREVIDÊNCIA quer fazer parte da sua história!</strong></p>
<p style="text-align: center;"><strong></strong>Veja abaixo alguns motivos para aderir, faça uma simulação e preencha o Termo de Adesão.</p>
<div class="accordian"><h5 class="toggle "><a href="#"><span class="arrow"></span>Motivos para Adesão</a></h5><div class="toggle-content " style="">Quer um ótimo motivo para fazer agora mesmo a sua adesão ao Plano SEBRAEPREV? Te damos <strong>sete</strong>!</p>
<p><b>1. O seu futuro.</b></p>
<p>Sim, o seu futuro, ele depende das suas escolhas, das decisões tomadas no presente. O futuro está bem ali, mais próximo do que podemos imaginar. Um futuro mais tranquilo depende de planejamento, depende de previdência complementar. Quer mais motivos? Então vamos lá.</p>
<p><b>2. A</b><b> contrapartida patronal.</b></p>
<p>Para cada R$1,00 que você contribuir mensalmente, até o limite de 7% aplicados sobre a parcela do salário de contribuição excedente a R$ 3.341,10, o SEBRAE contribuirá com o mesmo valor. Isso significa que o que você recolhe é duplicado a cada contribuição. Isso é o que chamamos de contrapartida patronal. O empregado que ganha abaixo de R$ 3.341,10 contribuirá com R$ 24,62 e também recebe contrapartida patronal no mesmo valor.</p>
<p><b>3. O benefício fiscal na hora de pagar o IR.</b></p>
<p>Ainda tem mais vantagens: o benefício fiscal, pois as contribuições, investidas no Plano SEBRAEPREV por você, podem ser reduzidas na declaração completa de ajuste anual do imposto de renda, até o limite de 12% dos rendimentos brutos recebidos no ano.</p>
<p><b>4. Caso você saia do SEBRAE, 4 opções estarão disponíveis para aplicar suas contribuições.</b></p>
<p><b>(1) Autopatrocínio: </b>opção por continuar no Plano SEBRAEPREV como autopatrocinado, assumindo as contribuições do SEBRAE; <b>(2) Benefício Proporcional Diferido (BPD):</b> você suspende as suas contribuições e opta por receber em tempo futuro o benefício de Aposentadoria Normal proporcional ao saldo acumulado; <b>(3) Resgate:</b> você poderá resgatar 100% das suas contribuições e 20% por ano completo de filiação ao Plano SEBRAEPREV das contribuições realizadas pelo SEBRAE, ou seja, após 5 anos também resgatará 100% da parte patronal; <b>(4) Portabilidade:</b> você tem a opção de transferir todos os recursos (as suas contribuições e as realizadas pelo SEBRAE) para um plano de benefícios (receptor) administrado por uma seguradora ou entidade (aberta ou fechada) de previdência complementar. Ou o contrário, o que é bastante comum no Plano SEBRAEPREV, participantes que possuem plano de previdência em outras entidades, portar a sua reserva para o Plano SEBRAEPREV.</p>
<p><b>5. Benefício de Risco.</b></p>
<p>Um dos benefícios do Plano SEBRAEPREV é o chamado Benefício de Risco. Ele existe para ajudar você a proteger a sua família em caso de eventos como invalidez ou morte. Caso o participante Ativo venha a falecer ou tornar-se inválido, ao seu saldo de conta total serão acrescidas as contribuições projetadas entre a data da ocorrência do evento até a data em que o participante cumpriria as condições para solicitar o benefício de Aposentadoria Normal. O Benefício de Risco (invalidez ou morte) será pago ao próprio participante ou aos seus beneficiários. Conforme o caso, em forma de pagamento único ou parcelado de acordo com as disposições do Regulamento do Plano SEBRAEPREV.</p>
<p><b>6. Três possibilidades de Perfil de Investimentos.</b></p>
<p>Se você prioriza um futuro tranquilo e seguro e quer ter a liberdade de escolher a forma de aplicação das suas contribuições, o Plano SEBRAEPREV oferece três possibilidades de perfis de investimentos: Perfil Conservador, Perfil Moderado (SEBRAEPREV) e o Perfil Arrojado. Com certeza um dos perfis deve ser adequado ao seu apetite e tolerância para correr riscos na aplicação de seus recursos. Consulte nosso portal <a href="http://www.sebraeprev.com.br/" onclick="javascript:_gaq.push(['_trackEvent','outbound-article','http://www.sebraeprev.com.br']);">www.sebraeprev.com.br</a> e obtenha maiores informações sobre os perfis de investimentos.</p>
<p><b>7. Empréstimo pessoal disponível.</b></p>
<p>Outra vantagem do Plano SEBRAEPREV é a possibilidade de empréstimo pessoal com taxas atualmente mais atraentes que aquelas praticadas por instituições financeiras.</p>
