<p>A Política de Investimento tem por objetivo estabelecer os princípios a serem seguidos e traçar as diretrizes que devem reger os investimentos dos Recursos Garantidores do Patrimônio Social (Provisões Matemáticas e Fundos) do SEBRAE PREVIDÊNCIA buscando garantir a segurança, liquidez e rentabilidade visando preservar os benefícios dos seus participantes e assistidos.</p>
